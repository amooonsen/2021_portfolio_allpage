// 우측 이미지 hover 시 scroll 되는 스크립트


// project 1 Interpark Main

$(function(){
	$('#project1 .wheelMain').mouseenter(function(){
		$('#project1 .wheelMain img').stop().animate({marginTop:"-1870px"},10500);
	}).mouseleave(function(){
		$('#project1 .wheelMain img').stop().animate({marginTop:"0px"},1000);
	});
});



